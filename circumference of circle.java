                     //Circumference of circle
import java.util.*;
public class Main{
    public static void Circumference(int r){
        double circumference=2*3.14*r;
        System.out.println(circumference);
        return;
    }
    
        
    
    public static  void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int r=sc.nextInt();
        Circumference(r);
        
        
    }
}
    


 